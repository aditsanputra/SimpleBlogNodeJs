const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const blogSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    snippet: {
        type: String,
        required: true
    },
    body: {
        type: String,
        required: true
    }
}, { timestamps: true });

//Blog = singular of Blogs which is the name of the collection in the database
const Blog = mongoose.model('Blog', blogSchema);
module.exports = Blog;